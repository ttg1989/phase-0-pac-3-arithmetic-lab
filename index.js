function add(a,b) // Add two numbers together and return the result.
{
    return a+b;
}

function subtract(a,b) // Subtract two numbers together and return the result.
{
    return a-b;
}

function multiply(a,b) // Multiply two numbers together and return the result.
{
    return a*b;
}

function divide(a,b) // Divide two numbers together and return the result.
{
    return a/b;
}

function increment(n) // Increment a number and return the result.
{
    n++;
    return n;
}

function decrement(n) // Decrement a number and return the result.
{
    n--
    return n;
}

function makeInt(n) // Parse n to a radix of 10.
{
    return parseInt(n,10);
}

function preserveDecimal(n) // Parse n as a float.
{
    return parseFloat(n);
}